#[derive(Clone, PartialEq, Eq, Ord)]
struct Node {
    row: usize,
    col: usize,
    time: i32,
}
impl Node {
    fn new(row: usize, col: usize, time: i32) -> Self {
        Node { row, col, time }
    }
}
impl PartialOrd for Node {
    fn partial_cmp(&self, o: &Self) -> Option<std::cmp::Ordering> {
        self.time.partial_cmp(&o.time)
    }
}

impl Solution {
    pub fn min_operations_max_profit(
        customers: Vec<i32>,
        boarding_cost: i32,
        running_cost: i32,
    ) -> i32 {
        let (mut resMax, mut resCnt, mut wait, mut cur, mut i) = (0, -1, 0, 0, 0);
        while wait != 0 || i < customers.len() {
            if i < customers.len() {
                wait += customers[i];
            }
            cur += wait.min(4) * boarding_cost - running_cost;
            wait = 0.max(wait - 4);
            if cur > resMax {
                resMax = cur;
                resCnt = i as i32 + 1;
            }
            i += 1;
        }
        resCnt
    }
}
