impl Solution {
    pub fn next_larger_nodes(head: Option<Box<ListNode>>) -> Vec<i32> {
        let mut tmp = &head;
        let mut idx = 0;
        let mut vlist = vec![];
        let mut res = vec![];
        let mut s = vec![];
        while let Some(ref node) = tmp {
            vlist.push(node.val);
            res.push(0);
            while !s.is_empty() && vlist[*s.last().unwrap()] < node.val {
                res[s.pop().unwrap()] = node.val;
            }
            s.push(idx);
            idx += 1;
            tmp = &node.next;
        }
        res
    }
}
